----- CREATE VISTA ----------
  CREATE MATERIALIZED VIEW "CONF_DWH"."DIM_CONCURSO" ("CONC_ID", "CONC_CONCURSO", "CONC_CONCURSO_DESC", "CONC_FECHA_INI_CONC", "CONC_FECHA_FIN_CONC", "CONC_ESTADO_CONC", "FECHA_REGISTRO", "ESTADO", "START_ESTADO", "END_ESTADO", "FECHA_CONTROL", "FECHA_INICIAL", "FECHA_FIN")
  ORGANIZATION HEAP PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 
NOCOMPRESS LOGGING
STORAGE(INITIAL 65536 NEXT 1048576 MINEXTENTS 1 MAXEXTENTS 2147483645
PCTINCREASE 0 FREELISTS 1 FREELIST GROUPS 1
BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
TABLESPACE "CONF_DWH"   NO INMEMORY 
BUILD IMMEDIATE
USING INDEX 
REFRESH COMPLETE ON DEMAND START WITH sysdate+0 NEXT SYSDATE + 1
USING DEFAULT LOCAL ROLLBACK SEGMENT
USING ENFORCED CONSTRAINTS DISABLE QUERY REWRITE
  AS SELECT SEQ_CONC_ID.NEXTVAL@BODEGA CONC_ID,
       C.CCODIGO CONC_CONCURSO,
       C.TDESCRIP CONC_CONCURSO_DESC, 
       C.FINICAM CONC_FECHA_INI_CONC, 
       C.FFINCAM CONC_FECHA_FIN_CONC,
       DECODE(C.CESTADO,0,'Activar',1,'Activa',2,'Finalizada',3,'Anulada') CONC_ESTADO_CONC,
       AXIS.F_SYSDATE FECHA_REGISTRO,
       'ACTIVO' ESTADO,
       TO_DATE('01/01/1986') START_ESTADO,
       AXIS.F_SYSDATE END_ESTADO,
       AXIS.F_SYSDATE FECHA_CONTROL,
       TRUNC(AXIS.F_SYSDATE, 'month') FECHA_INICIAL,
       TRUNC(LAST_DAY(AXIS.F_SYSDATE)) FECHA_FIN
   FROM AXIS.CAMPANAS C;

   COMMENT ON MATERIALIZED VIEW "CONF_DWH"."DIM_CONCURSO"  IS 'snapshot table for snapshot DWH_CONF.DIM_CONCURSO';
