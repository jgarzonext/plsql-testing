----- CREATE VISTA ----------
CREATE MATERIALIZED VIEW "CONF_DWH"."DIM_TIPO_CERTIFICADO" ("TCE_ID", "TCE_TIPO_CERT", "TCE_TIPO_CERT_DESC", "FECHA_REGISTRO", "ESTADO", "START_ESTADO", "END_ESTADO", "FECHA_CONTROL", 
"FECHA_INICIAL", "FECHA_FIN")
ORGANIZATION HEAP PCTFREE 10 PCTUSED 40 INITRANS 1 MAXTRANS 255 
NOCOMPRESS LOGGING
STORAGE(INITIAL 65536 NEXT 1048576 MINEXTENTS 1 MAXEXTENTS 2147483645
PCTINCREASE 0 FREELISTS 1 FREELIST GROUPS 1
BUFFER_POOL DEFAULT FLASH_CACHE DEFAULT CELL_FLASH_CACHE DEFAULT)
TABLESPACE "CONF_DWH"   NO INMEMORY 
BUILD IMMEDIATE
USING INDEX 
REFRESH COMPLETE ON DEMAND START WITH SYSDATE+0 NEXT SYSDATE + 1
USING DEFAULT LOCAL ROLLBACK SEGMENT
USING ENFORCED CONSTRAINTS DISABLE QUERY REWRITE
AS SELECT d.catribu TCE_ID,
d.catribu TCE_TIPO_CERT, 
d.tatribu TCE_TIPO_CERT_DESC,
AXIS.F_SYSDATE FECHA_REGISTRO, 
'ACTIVO' ESTADO,
AXIS.F_SYSDATE START_ESTADO,
AXIS.F_SYSDATE END_ESTADO,
AXIS.F_SYSDATE FECHA_CONTROL,
TRUNC(AXIS.F_SYSDATE, 'MONTH') FECHA_INICIAL,
TRUNC(LAST_DAY(SYSDATE)) FECHA_FIN
from AXIS.detvalores d
where d.cvalor = 16 
and d.cidioma = 8;

COMMENT ON MATERIALIZED VIEW "CONF_DWH"."DIM_TIPO_CERTIFICADO"  IS 'snapshot table for snapshot DWH_CONF.DIM_TIPO_CERTIFICADO';
