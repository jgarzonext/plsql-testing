CREATE OR REPLACE TRIGGER TRG_INS_MOVCONTASAP_ANULA_REC
  AFTER UPDATE ON MOVSEGURO
  FOR EACH ROW
  BEGIN
   IF :NEW.CMOVSEG IN (52) THEN
      PAC_CONTA_SAP.MOVCONTASAP_ANULA_REC(:NEW.SSEGURO,:NEW.NMOVIMI);
   END IF;

  EXCEPTION
   WHEN OTHERS THEN
      P_TAB_ERROR(F_SYSDATE,
                  F_USER,
                  'TRG_INS_MOVCONTASAP_ANULA_REC',
                  0,
                  SQLCODE,
                  SQLERRM);
END;
