CREATE OR REPLACE TRIGGER TRG_INS_MOVCONTASAPDET
  AFTER INSERT ON MOVSEGURO
  FOR EACH ROW
  BEGIN
   IF :NEW.CMOTMOV IN (321) THEN
      PAC_CONTA_SAP.MOVCONTA_SCENARIO(:NEW.SSEGURO,:NEW.CMOTMOV);
   END IF;

  EXCEPTION
   WHEN OTHERS THEN
      P_TAB_ERROR(F_SYSDATE,
                  F_USER,
                  'TRG_INS_MOVCONTASAPDET',
                  0,
                  SQLCODE,
                  SQLERRM);
END;
