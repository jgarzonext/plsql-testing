CREATE OR REPLACE EDITIONABLE TRIGGER VALIDA_FGENERA
   BEFORE INSERT OR UPDATE ON CESIONESREA
   FOR EACH ROW   
BEGIN        
    IF :new.FGENERA IS NULL THEN
        :NEW.FGENERA := TRUNC(SYSDATE);
    END IF;
EXCEPTION
   WHEN OTHERS THEN
      p_tab_error(f_sysdate, f_user, 'ERROR AL INSERTAR O ACTUALIZAR LA FECHA DE GENERACION EN CESIONESREA', 2, SQLCODE, SQLERRM);     
END;
/
ALTER TRIGGER VALIDA_FGENERA ENABLE;
/
COMMIT;