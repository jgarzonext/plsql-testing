CREATE OR REPLACE TRIGGER TRG_INS_MOVCONTASAP_ANULA
  AFTER INSERT ON MOVRECIBO
  FOR EACH ROW
  BEGIN
   IF :NEW.CESTREC = 2  AND :NEW.CMOTMOV IN (52,306) THEN
      PAC_CONTA_SAP.MOVCONTA_SCENARIO_ANULA(:NEW.NRECIBO,:NEW.CMOTMOV,:NEW.CESTREC);
   END IF;

  EXCEPTION
   WHEN OTHERS THEN
      P_TAB_ERROR(F_SYSDATE,
                  F_USER,
                  'TRG_INS_MOVCONTASAP_ANULA',
                  0,
                  SQLCODE,
                  SQLERRM);
END;