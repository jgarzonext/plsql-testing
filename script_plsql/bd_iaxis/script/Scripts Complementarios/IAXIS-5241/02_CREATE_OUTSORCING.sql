-- CREATE TABLE
CREATE TABLE OUTSORCING_EXT(
NRECIBO       NUMBER,
APLICA_OUT    VARCHAR2(4),
CGESCAR       NUMBER,
CPROCES       NUMBER,
FCARGA        DATE );

-- ADD COMMENTS TO THE COLUMNS 
COMMENT ON COLUMN OUTSORCING_EXT.NRECIBO
  IS 'Número de recibo.';
COMMENT ON COLUMN OUTSORCING_EXT.APLICA_OUT
  IS '1. Directo / 2. Outsorcing';
COMMENT ON COLUMN OUTSORCING_EXT.CGESCAR
  IS '1. NO / 2. SI';
COMMENT ON COLUMN OUTSORCING_EXT.CPROCES
  IS 'Proceso de Carga';
COMMENT ON COLUMN OUTSORCING_EXT.FCARGA
  IS 'Fecha de Carga';
 COMMIT; 
