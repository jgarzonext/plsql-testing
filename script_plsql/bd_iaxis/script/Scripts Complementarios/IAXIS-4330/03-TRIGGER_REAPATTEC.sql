CREATE OR REPLACE TRIGGER "WHO_REA_PAT_TEC"
 BEFORE INSERT OR UPDATE ON REAPATTEC
 FOR EACH ROW
BEGIN
    IF :OLD.NANIO IS NULL AND :OLD.NTRIM IS NULL THEN     -- (Es insert)
      :NEW.CUSUALT := F_USER;
      :NEW.FUSUALT   := F_SYSDATE;
    ELSE                -- (Es update)
      :NEW.CUSUMOD := F_USER;
      :NEW.FUSUMOD := F_SYSDATE;
    END IF;
END;

commit;