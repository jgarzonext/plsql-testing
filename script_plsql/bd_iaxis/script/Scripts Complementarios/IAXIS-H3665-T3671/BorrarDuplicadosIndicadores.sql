/* Borrado de registros duplicados en */
DELETE FROM FIN_INDICADORES E
WHERE NOT EXISTS (SELECT D.* 
FROM FIN_INDICADORES D LEFT OUTER JOIN (SELECT SFINANCI, MAX(NMOVIMI) AS MOVIMIENTO FROM (SELECT A.SFINANCI, A.FINDICAD, A.NMOVIMI
FROM FIN_INDICADORES A, FIN_INDICADORES B
WHERE A.SFINANCI = B.SFINANCI
AND A.FINDICAD = B.FINDICAD
ORDER BY A.SFINANCI, A.FINDICAD) ABC
GROUP BY SFINANCI
) BB ON D.SFINANCI = BB.SFINANCI AND D.NMOVIMI = BB.MOVIMIENTO
WHERE D.SFINANCI = BB.SFINANCI AND D.NMOVIMI = BB.MOVIMIENTO
AND D.SFINANCI = E.SFINANCI AND D.NMOVIMI = E.NMOVIMI
);
COMMIT;
