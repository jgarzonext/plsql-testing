  CREATE OR REPLACE EDITIONABLE FUNCTION "AXIS"."F_GET_TF_DOCUADMIN_PATH" 
  (pTCLAVE IN VARCHAR2)
RETURN VARCHAR
AUTHID CURRENT_USER
IS
  vPATH  VARCHAR2(500);
  vFILENAME VARCHAR2(150);
BEGIN
  SELECT tvalor
    INTO vFILENAME
    FROM tf_docuadmin
   WHERE tclave = UPPER(pTCLAVE);

  vPATH := f_parinstalacion_t('TFDOCUADM')||vFILENAME;

  RETURN vPATH;

EXCEPTION
  WHEN OTHERS THEN
    RETURN NULL;
END;

 
 

/

