:OLD.CGARANT,
:OLD.FFECINI,
:OLD.CODGRUP,
:OLD.FFECFIN,
:OLD.CUSUALT,
:OLD.FALTA,
:OLD.CUSUMOD,
:OLD.FMODIFI,
f_user, f_sysdate, ''||vaccion||'');
EXCEPTION
   WHEN OTHERS THEN
      p_tab_error(f_sysdate, f_user, 'TRIGGER trg_his_BF_PROGARANGRUP', 1, SQLCODE, SQLERRM);
