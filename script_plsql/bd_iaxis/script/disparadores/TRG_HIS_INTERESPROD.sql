  CREATE OR REPLACE EDITIONABLE TRIGGER "AXIS"."TRG_HIS_INTERESPROD" 
   BEFORE UPDATE OR DELETE
   ON INTERESPROD
   FOR EACH ROW
   DECLARE
       vaccion VARCHAR2(2);
BEGIN
   IF UPDATING THEN
       vaccion := 'U';
   ELSE
       vaccion := 'D';
   END IF;

      -- crear registro histórico
      INSERT INTO his_INTERESPROD(
CRAMO,
CCOLECT,
CTIPSEG,
CMODALI,
FINIVIG,
FFINVIG,
PINTTOT,
PINTEMP,
CUSUALT,
FALTA,
CUSUMOD,
FMODIFI,
CUSUHIST,FCREAHIST,ACCION)
VALUES(
:OLD.CRAMO,
:OLD.CCOLECT,
:OLD.CTIPSEG,
:OLD.CMODALI,
:OLD.FINIVIG,
:OLD.FFINVIG,
:OLD.PINTTOT,
:OLD.PINTEMP,
:OLD.CUSUALT,
:OLD.FALTA,
:OLD.CUSUMOD,
:OLD.FMODIFI,
f_user, f_sysdate, ''||vaccion||'');
EXCEPTION
   WHEN OTHERS THEN
      p_tab_error(f_sysdate, f_user, 'TRIGGER trg_his_INTERESPROD', 1, SQLCODE, SQLERRM);
      RAISE;
END trg_his_INTERESPROD;





/
ALTER TRIGGER "AXIS"."TRG_HIS_INTERESPROD" ENABLE;
