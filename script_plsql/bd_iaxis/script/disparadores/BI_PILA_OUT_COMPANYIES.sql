--------------------------------------------------------
--  DDL for Trigger BI_PILA_OUT_COMPANYIES
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "AXIS"."BI_PILA_OUT_COMPANYIES" 
BEFORE INSERT ON PILA_OUT_COMPANYIES
FOR EACH ROW
DECLARE
BEGIN
	IF :NEW.PILAPK IS NULL THEN
		SELECT PILAOUT_SEQ.NEXTVAL
		INTO :NEW.PILAPK
		FROM DUAL;
	END IF;
END BI_PILA_OUT_COMPANYIES;









/
ALTER TRIGGER "AXIS"."BI_PILA_OUT_COMPANYIES" ENABLE;
