TFUNCIO,
CMOTMOV,
COBLIGEDOX,
CTIPDEST,
CUSUALT,
FALTA,
CUSUMOD,
FMODIFI,
CUSUHIST,FCREAHIST,ACCION)
VALUES(
:OLD.CDOCUME,
:OLD.SPRODUC,
:OLD.CACTIVI,
:OLD.NORDEN,
:OLD.CTIPDOC,
:OLD.CCLASE,
:OLD.TFUNCIO,
:OLD.CMOTMOV,
:OLD.COBLIGEDOX,
:OLD.CTIPDEST,
:OLD.CUSUALT,
:OLD.FALTA,
:OLD.CUSUMOD,
:OLD.FMODIFI,
f_user, f_sysdate, ''||vaccion||'');
EXCEPTION
   WHEN OTHERS THEN
      p_tab_error(f_sysdate, f_user, 'TRIGGER trg_his_DOC_DOCUREQUERIDA', 1, SQLCODE, SQLERRM);
      RAISE;
END trg_his_DOC_DOCUREQUERIDA;





