  CREATE OR REPLACE EDITIONABLE TRIGGER "AXIS"."TRG_HIS_DOC_DOCUREQUERIDA" 
   BEFORE UPDATE OR DELETE
   ON DOC_DOCUREQUERIDA
   FOR EACH ROW
   DECLARE
       vaccion VARCHAR2(2);
BEGIN
   IF UPDATING THEN
       vaccion := 'U';
   ELSE
       vaccion := 'D';
   END IF;

      -- crear registro histórico
